int superDigit(string n, int k) {
    long super = 0;
    
    for (char c : n){
        int num = c-'0';
        super += num; 
    }
    
    super= super * k;
    
    if (to_string(super).size()>1){
        return  superDigit(to_string(super), 1);
    }else{
        return super;    
    }   
}

