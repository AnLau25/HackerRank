void vectorize(vector<int> stack, vector<int> &vec){
    int sum = 0;
    
    for (int i = stack.size() - 1; i >= 0; --i) {
        sum += stack[i];
        vec.push_back(sum);
    }
}

int equalStacks(vector<int> h1, vector<int> h2, vector<int> h3) {
    if(h1.empty() || h2.empty() || h3.empty()) return 0;
    
    vector<int> v1, v2, v3, temp, finale;
    
    vectorize(h1, v1);
    vectorize(h2, v2);
    vectorize(h3, v3);
    
    std::set_intersection(
        v1.begin(), v1.end(),
        v2.begin(), v2.end(),
        std::back_inserter(temp)
    );
    
    std::set_intersection(
        temp.begin(), temp.end(),
        v3.begin(), v3.end(),
        std::back_inserter(finale)
    );
    
    if(!finale.empty()){
        return finale.back();
    }
    
    return 0;
}

