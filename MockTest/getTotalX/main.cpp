bool isProduct(int x, vector<int> a){
    for (int i : a){
        if (x%i!=0){
            return false;
        }
    }
    return true;
}

bool isFactor(int x, vector<int> b){
    for (int i : b){
        if (i%x!=0){
            return false;
        }
    }
    return true;
}

int getTotalX(vector<int> a, vector<int> b) {
    int count = 0;
    sort(a.begin(),a.end());
    sort(b.begin(),b.end());
    int smallest = a.back();
    int biggest = b.front();
    
    while (smallest<=biggest) {
        
        if(isProduct(smallest, a)&&isFactor(smallest, b)){
            count++;
        }
        
        smallest++;
        
    }
    
    return count;
}

