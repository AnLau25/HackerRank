string funnyString(string s) {
    string z = s;
    reverse(z.begin(),z.end()); 
    
    for(int i = 1; i<s.size(); i++){
        if(abs(s[i]-s[i-1])!=abs(z[i]-z[i-1])){
            return "Not Funny";
        }
    }
    
    return "Funny";
}

