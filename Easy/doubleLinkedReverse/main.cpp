DoublyLinkedListNode* reverse(DoublyLinkedListNode* llist) {
    std::swap(llist->next,llist->prev);
    
    while(llist->prev!=nullptr){
        llist= llist->prev;
        std::swap(llist->next,llist->prev);
    }
    
    return llist;
}

