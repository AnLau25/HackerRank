int marsExploration(string s) {
    int errs = 0;
    
    for (size_t i = 0; i < s.size(); i += 3) {
        string sos = s.substr(i, 3);
        
        if(sos!="SOS"){
            if(sos[0]!='S'){
                errs++; 
            }
            if(sos[1]!='O'){
                errs++; 
            }
            if(sos[2]!='S'){
                errs++; 
            }
        }
    }
    return errs;

}

