long repeatedString(string s, long n) {
    long len = s.size();
    long count_a = 0;
    
    for (char c : s){
        if(c=='a'){
            count_a++;
        }
    }
    
    long a_perword = (n/len)*count_a;
    
    if(n%len==0){
        return a_perword;
    } 
    
    for (int j = 0; j<(n%len); j++) {
        if(s[j]=='a'){
            a_perword++;
        }
    }
    
    return a_perword;
}

