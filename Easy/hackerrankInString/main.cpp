string hackerrankInString(string s) {
    string rank = "hackerrank";
    int trav = 0;
    
    for(int i = 0; i<s.size(); i++){
        if(trav<rank.size() && s[i]==rank[trav]){
            trav++;
        }
    }
    
    if(trav==rank.size()){
        return "YES";
    }
    
    return "NO";
}

