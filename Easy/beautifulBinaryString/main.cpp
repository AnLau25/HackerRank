int beautifulBinaryString(string b) {
    int ptrn = 0;
    
    for (int i = 0; i<b.size(); i++){
        string sub = b.substr(i, 3);
        
        if(sub=="010"){
            ptrn++;
            i+=2;
        }
    }
    
    return ptrn;
}

