/*
 * Complete the 'howManyGames' function below.
 *
 * The function is expected to return an INTEGER.
 * The function accepts following parameters:
 *  1. INTEGER p
 *  2. INTEGER d
 *  3. INTEGER m
 *  4. INTEGER s
 */

int howManyGames(int p, int d, int m, int s) {
    int games = 0;
    
    while(p>m && s>0){
        
        s-=p;
        
        if(s>0){
            games++;
            p-=d;
        }
    }
    
    if((s-m)>=0){
        return games + (s/m);
    }
    
    return games;
}

